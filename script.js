// 2. ボタンと結果を表示する場所を「覚える」
const drawButton = document.getElementById('draw-button');
const resultArea = document.getElementById('result-area');

// 3. 占いを行うための関数（処理のかたまり）
function drawLuckyWord() {
    // リストの長さ（この場合8）を使って、0から7までのランダムな数字を作る
    const randomIndex = Math.floor(Math.random() * luckyWords.length);

    // そのランダムな数字を使って、リストからラッキーワードを1つ選ぶ
    const word = luckyWords[randomIndex];

    // 結果の表示を更新する
    resultArea.innerHTML = `<p class="result-word">${word}</p>`;
}

// 4. ボタンがクリックされたら、↑の占い処理を実行するように「約束」する
drawButton.addEventListener('click', drawLuckyWord);